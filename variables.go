package main 

import "fmt"

func main() {
	var text string = "initial"
	fmt.Println("text = ", text);

	var a, b int = 1, 2
	fmt.Println("a and b is = ", a, b);

	var d = true
	fmt.Println("d = ", d);

	var e int = 10
	fmt.Println("e = ", e);
	second := "second";
	fmt.Println("second = ", second);
}